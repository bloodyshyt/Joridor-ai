package Util;


public class Str {

	public static void println(String s) {
		System.out.println(s);
	}
	
	public static void println() {
		System.out.println();
	}
	
	public static void print(String s) {
		System.out.print(s);
	}
	
	public static String ptToStr(int x, int y) {
		return new String("(" + x + "," + y + ")");
	}
	
	
}
